package chapter3;

public class ForEx1 {

	public static void main(String[] args) {
		// 备备窜 2窜
		
		System.out.println("备备窜 2窜");
		for(int i = 1; i <= 9; i++) {
			System.out.println("2 X " + i + " = " + (2*i));
		}
		
		
		System.out.println("2 X 1 = 2");
		System.out.println("2 X 2 = 4");
		System.out.println("2 X 3 = 6");
		System.out.println("2 X 4 = 8");
		

	}

}
