package chapter1;

public class VariableEx2 {

	public static void main(String[] args) {
		char c1 = '안';
		char c2 = '녕';
		char c3 = '하';
		char c4 = '세';
		char c5 = '요';
		
		System.out.println("" + c1 + c2 + c3 + c4 + c5 + ".");
		
		char name1 = '김';
		char name2 = '준';
		char name3 = '일';
		int year = 2021;
		int month = 5;
		int day = 12;
		
		System.out.print(name1);
		
		System.out.println("" + name1 + name2 + name3 + "의 생일은 " + year + "." + month + "." + day + "입니다.");
	}

}
